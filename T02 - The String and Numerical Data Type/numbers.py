num1 = int(input("Enter the first integer: "))
num2 = int(input("Enter the second integer: "))
num3 = int(input("Enter the third integer: "))

sum_of_numbers = num1 + num2 + num3
print("Sum of all numbers:", sum_of_numbers)

difference_first_second = num1 - num2
print("First number minus the second number:", difference_first_second)

product_first_third = num1 * num3
print("Third number multiplied by the first number:", product_first_third)

sum_divided_by_third = (num1 + num2 + num3) / num3
print("Sum of all three numbers divided by the third number:", sum_divided_by_third)
